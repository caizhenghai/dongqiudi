package com.hzr.project.model;

public class LplGame {
    private String Year;
    private String Team;
    private String Season;
    private int Rank;
    private int Appearances;
    private String WinLose;
    private String WinRate;
    private String TotalKills;
    private String TotalDeath;
    private int AverageEyes;
    private int AverageKillEyes;
    private int AverageMoney;
    private double AverageSmallDragon;
    private double AverageBigDragon;

    public String getYear() {
        return Year;
    }

    public void setYear(String year) {
        Year = year;
    }

    public String getTeam() {
        return Team;
    }

    public void setTeam(String team) {
        Team = team;
    }

    public String getSeason() {
        return Season;
    }

    public void setSeason(String season) {
        Season = season;
    }

    public int getRank() {
        return Rank;
    }

    public void setRank(int rank) {
        Rank = rank;
    }

    public int getAppearances() {
        return Appearances;
    }

    public void setAppearances(int appearances) {
        Appearances = appearances;
    }

    public String getWinLose() {
        return WinLose;
    }

    public void setWinLose(String winLose) {
        WinLose = winLose;
    }

    public String getWinRate() {
        return WinRate;
    }

    public void setWinRate(String winRate) {
        WinRate = winRate;
    }

    public String getTotalKills() {
        return TotalKills;
    }

    public void setTotalKills(String totalKills) {
        TotalKills = totalKills;
    }

    public String getTotalDeath() {
        return TotalDeath;
    }

    public void setTotalDeath(String totalDeath) {
        TotalDeath = totalDeath;
    }

    public int getAverageEyes() {
        return AverageEyes;
    }

    public void setAverageEyes(int averageEyes) {
        AverageEyes = averageEyes;
    }

    public int getAverageKillEyes() {
        return AverageKillEyes;
    }

    public void setAverageKillEyes(int averageKillEyes) {
        AverageKillEyes = averageKillEyes;
    }

    public int getAverageMoney() {
        return AverageMoney;
    }

    public void setAverageMoney(int averageMoney) {
        AverageMoney = averageMoney;
    }

    public double getAverageSmallDragon() {
        return AverageSmallDragon;
    }

    public void setAverageSmallDragon(double averageSmallDragon) {
        AverageSmallDragon = averageSmallDragon;
    }

    public double getAverageBigDragon() {
        return AverageBigDragon;
    }

    public void setAverageBigDragon(double averageBigDragon) {
        AverageBigDragon = averageBigDragon;
    }
}
